# 6. Herramientas del agente
# ------------------------------------------------------------
class ReporteFalla(BaseModel):
    id_equipo: str
    descripcion: str

@tool
def procesar_reporte(texto: str) -> dict:
    """Procesa el texto de un reporte de falla para extraer el ID del equipo y la descripción."""
    for eid in equipos["ID"]:
        if eid in texto:
            return {"id_equipo": eid, "descripcion": texto}
    return {"error": "ID no encontrado"}

@tool
def integrar_datos(id_equipo: str) -> dict:
    """Integra datos del equipo y sus repuestos basándose en el ID del equipo."""
    eq = equipos[equipos["ID"] == id_equipo].iloc[0]
    rep = repuestos[repuestos["ID_Equipo"] == id_equipo].iloc[0]
    return {
        "id_equipo": id_equipo,
        "nombre": eq["Nombre"],
        "ubicacion": eq["Ubicacion"],
        "criticidad": eq["Criticidad"],
        "historial": eq["Historial"],
        "repuesto": rep["Repuesto"],
        "stock": rep["Stock"],
        "tiempo_reposicion": rep["TiempoReposicion"]
    }

@tool
def evaluar_criticidad(datos: dict) -> dict:
    """Evalúa la criticidad de una falla y genera alertas preventivas si el stock es cero."""
    criticidad = datos["criticidad"]
    alerta = None
    if criticidad in ["Baja", "Media"] and datos["stock"] == 0:
        alerta = "⚠️ Esta falla podría volverse media en 1 semana (sin repuesto disponible)."
    return {"criticidad_final": criticidad, "alerta": alerta}

@tool
def generar_informe(datos: dict, decision: dict) -> str:
    """Genera un informe ejecutivo en formato PDF con los detalles de la falla y la decisión."""
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", size=12)
    criticidad_final = decision.get("criticidad_final", datos.get("criticidad", "No definida"))
    alerta = decision.get("alerta", "Ninguna")
    encabezado = f"Atención de falla del equipo {datos['id_equipo']} – {criticidad_final}"
    pdf.cell(200, 10, encabezado, ln=True, align="C")
    pdf.ln(10)
    pdf.multi_cell(0, 10, f"""
Empresa: Hidrocarburos del Perú
Equipo: {datos['nombre']}
Ubicación: {datos['ubicacion']}
Criticidad oficial: {datos['criticidad']}
Historial: {datos['historial']}
Repuesto: {datos['repuesto']} | Stock: {datos['stock']} | Tiempo reposición: {datos['tiempo_reposicion']}
Acción recomendada: Realizar el trabajo correctivo según la criticidad del equipo
Alerta preventiva: {alerta}
Fecha: {datetime.date.today()}
Responsable: Ingeniero de campo 1
""")
    nombre_pdf = f"informe_{datos['id_equipo']}.pdf"
    pdf.output(nombre_pdf)
    return f"Informe generado: {nombre_pdf}"

