# Middleware de Validación y Redirección con @before_agent
# ============================================================

@before_agent(can_jump_to=["end"])
def validar_reporte(state, runtime):
    """
    Middleware que valida ID y ubicación antes de que el agente procese el reporte.
    Si hay error, corta el flujo y devuelve mensaje de verificación.
    """
    # Extraer texto del usuario
    texto_usuario = ""
    for msg in state["messages"]:
        if msg.type == "human":
            texto_usuario = msg.content

    # Normalizar texto
    texto_usuario = texto_usuario.strip().upper()

    # Validar ID de equipo
    id_valido = None
    for eid in equipos["ID"].astype(str):
        eid_norm = eid.strip().upper()
        if eid_norm in texto_usuario:
            id_valido = eid
            break

    if not id_valido:
        return {
            "messages": [SystemMessage(content="⚠️ Error: El ID de equipo no es válido o no existe. Por favor verificar")],
            "jump_to": "end"
        }

    # Validar ubicación vinculada al ID
    ubicacion = str(equipos[equipos["ID"] == id_valido]["Ubicacion"].values[0]).strip().upper()
    if ubicacion not in texto_usuario:
        return {
            "messages": [
                SystemMessage(
                    content=f"⚠️ Error: La ubicación reportada no coincide con el ID {id_valido}.\n"
                            f"Reporte redirigido a verificación."
                )
            ],
            "jump_to": "end"
        }

    # Si todo está correcto, continuar flujo normal
    return None

