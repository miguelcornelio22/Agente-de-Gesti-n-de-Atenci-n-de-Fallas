# 4. Middleware de Confirmación de Acciones Sensibles
# ------------------------------------------------------------
class ConfirmacionMiddleware(AgentMiddleware):
    """
    Middleware que confirma antes de generar órdenes inmediatas en criticidad alta.
    """
    def wrap_model_call(self, request: ModelRequest, handler: callable) -> ModelResponse:
        texto_usuario = ""
        for msg in request.messages:
            if msg.type == "human":
                texto_usuario = msg.content

        if "criticidad alta" in texto_usuario.lower():
            confirmacion = SystemMessage(
                content="⚠️ Confirmación requerida: ¿Desea generar la orden inmediata para criticidad alta?"
            )
            return ModelResponse(result=[confirmacion])

        return handler(request)

# ------------------------------------------------------------
# 5. Middleware de Fallback
# ------------------------------------------------------------
class FallbackMiddleware(AgentMiddleware):
    """
    Middleware que devuelve un mensaje seguro si el agente no puede resolver.
    """
    def wrap_model_call(self, request: ModelRequest, handler: callable) -> ModelResponse:
        try:
            return handler(request)
        except Exception:
            mensaje_fallback = SystemMessage(
                content="⚠️ No se pudo determinar la criticidad del equipo. Verifique el ID o la ubicación."
            )
            return ModelResponse(result=[mensaje_fallback])

